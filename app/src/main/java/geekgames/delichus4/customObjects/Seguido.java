package geekgames.delichus4.customObjects;

public class Seguido {

    public int id;
    public String foto;
    public String nombre;
    public String titulo;
    public int lvl;


    public Seguido(int id, String foto, String nombre, String titulo, int lvl) {
        this.id = id;
        this.foto = foto;
        this.nombre = nombre;
        this.titulo = titulo;
        this.lvl = lvl;
    }


}